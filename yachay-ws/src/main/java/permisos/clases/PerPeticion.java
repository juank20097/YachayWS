package permisos.clases;

public class PerPeticion {
	private String usr;
	private String pwd;
	private String apl;
	
	public PerPeticion() {
	}
	
	public String getUsr() {
		return usr;
	}
	public void setUsr(String usr) {
		this.usr = usr;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getApl() {
		return apl;
	}
	public void setApl(String apl) {
		this.apl = apl;
	}
	
	
}
